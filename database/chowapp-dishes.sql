-- SCHEMA
CREATE TABLE IF NOT EXISTS dishes (
  dish_id          VARCHAR(6)    PRIMARY KEY,
  slug             VARCHAR(100)  NOT NULL UNIQUE,
  name_english     VARCHAR(200)  NOT NULL,
  name_local       TEXT,
  category         VARCHAR(50)   NOT NULL,
  region           VARCHAR(100)  NOT NULL,
  ethnic_group     VARCHAR(100)  NOT NULL,
  primary_ingredients  TEXT[]    NOT NULL,
  taste_profile    TEXT[]        NOT NULL,
  description      TEXT          NOT NULL,
  pairs_with       TEXT[]        DEFAULT '{}',
  vegan            BOOLEAN       DEFAULT FALSE,
  vegetarian       BOOLEAN       DEFAULT FALSE,
  halal            BOOLEAN       DEFAULT TRUE,
  contains_pork    BOOLEAN       DEFAULT FALSE,
  contains_shellfish BOOLEAN     DEFAULT FALSE,
  spice_level      SMALLINT      CHECK (spice_level BETWEEN 0 AND 5),
  prep_time_mins   INTEGER,
  image_url        VARCHAR(300),
  created_at       TIMESTAMPTZ   DEFAULT NOW(),
  updated_at       TIMESTAMPTZ   DEFAULT NOW()
);

-- INDEXES
CREATE INDEX IF NOT EXISTS idx_dishes_category ON dishes(category);
CREATE INDEX IF NOT EXISTS idx_dishes_region ON dishes(region);
CREATE INDEX IF NOT EXISTS idx_dishes_ethnic_group ON dishes(ethnic_group);
CREATE INDEX IF NOT EXISTS idx_dishes_slug ON dishes(slug);
CREATE INDEX IF NOT EXISTS idx_dishes_halal ON dishes(halal);
CREATE INDEX IF NOT EXISTS idx_dishes_vegan ON dishes(vegan);
CREATE INDEX IF NOT EXISTS idx_dishes_spice ON dishes(spice_level);

