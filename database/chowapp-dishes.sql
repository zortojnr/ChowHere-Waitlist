-- RESTAURANTS SCHEMA (Unit 3 prep)
CREATE TABLE IF NOT EXISTS restaurants (
  restaurant_id    SERIAL        PRIMARY KEY,
  slug             VARCHAR(150)  NOT NULL UNIQUE,
  name             VARCHAR(200)  NOT NULL,
  type             VARCHAR(50),
  city             VARCHAR(100)  NOT NULL,
  neighbourhood    VARCHAR(100),
  address          TEXT,
  latitude         DECIMAL(9,6),
  longitude        DECIMAL(9,6),
  phone            VARCHAR(20),
  price_range      VARCHAR(10),
  delivery_available BOOLEAN     DEFAULT FALSE,
  verified         BOOLEAN       DEFAULT FALSE,
  claimed          BOOLEAN       DEFAULT FALSE,
  rating           DECIMAL(2,1)  DEFAULT 0.0,
  accuracy_score   SMALLINT      DEFAULT 0,
  created_at       TIMESTAMPTZ   DEFAULT NOW(),
  updated_at       TIMESTAMPTZ   DEFAULT NOW()
);

CREATE TABLE IF NOT EXISTS restaurant_dishes (
  id             SERIAL   PRIMARY KEY,
  restaurant_id  INTEGER  REFERENCES restaurants(restaurant_id) ON DELETE CASCADE,
  dish_id        VARCHAR(6) REFERENCES dishes(dish_id) ON DELETE CASCADE,
  available      BOOLEAN  DEFAULT TRUE,
  price_ngn      INTEGER,
  UNIQUE(restaurant_id, dish_id)
);

CREATE TABLE IF NOT EXISTS reviews (
  review_id       SERIAL      PRIMARY KEY,
  restaurant_id   INTEGER     REFERENCES restaurants(restaurant_id) ON DELETE CASCADE,
  dish_id         VARCHAR(6)  REFERENCES dishes(dish_id),
  dish_accuracy   VARCHAR(10) CHECK (dish_accuracy IN ('yes','no','partial')),
  food_quality    SMALLINT    CHECK (food_quality BETWEEN 1 AND 5),
  value_for_money SMALLINT    CHECK (value_for_money BETWEEN 1 AND 5),
  service         SMALLINT    CHECK (service BETWEEN 1 AND 5),
  review_text     TEXT,
  visit_date      DATE,
  created_at      TIMESTAMPTZ DEFAULT NOW()
);

